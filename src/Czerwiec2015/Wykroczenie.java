package Czerwiec2015;

public class Wykroczenie {
    public Wykroczenie(int kod_wyk, String nazwa, int mandat, int punkty) {
        this.kod_wyk = kod_wyk;
        this.nazwa = nazwa;
        this.mandat = mandat;
        this.punkty = punkty;
    }

    public int kod_wyk;
    public String nazwa;
    public int mandat;
    public int punkty;
}
