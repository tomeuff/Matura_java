package Maj2019_stara;

public class Aktor {
    public Aktor(String id_aktora, String imie, String nazwisko, String data_ur, String kraj_urodzenia) {
        this.id_aktora = id_aktora;
        this.imie = imie;
        this.nazwisko = nazwisko;
        this.data_ur = data_ur;
        this.kraj_urodzenia = kraj_urodzenia;
    }

    public String id_aktora;
    public String imie;
    public String nazwisko;
    public String data_ur;
    public String kraj_urodzenia;
}
